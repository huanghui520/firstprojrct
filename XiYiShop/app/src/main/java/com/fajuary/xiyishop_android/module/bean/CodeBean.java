public class CodeBean {

    /**
     * code : 200
     * date : 获取验证码成功！
     * msg : 请求成功！
     */

    private String code;
    private String date;
    private String msg;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
